// This is the click action for the What we do section
$(document).ready(function(){
    $(".contentsee1").click(function(){
        $(this).toggle(function(){
            $(".contenthide1").toggle();
        });
    })
    $(".contenthide1").click(function(){
        $(this).toggle(function(){
            $(".contentsee1").toggle();
        });
    })
})

